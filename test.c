void sprites(void)
{    
    unsigned int i;
    
    // clear sprite ram
    for( i = 0; i < 256; i++ )
    {
        *((unsigned char*)(0x200 + i)) = 0x00;
    }        
        
    // From NESDev:
    //+------+----------+--------------------------------------+
    //+ Byte | Bits     | Description                          |
    //+------+----------+--------------------------------------+
    //|  0   | YYYYYYYY | Y Coordinate - 1. Consider the coor- |
    //|      |          | dinate the upper-left corner of the  |
    //|      |          | sprite itself.                       |
    //|  1   | IIIIIIII | Tile Index #                         |
    //|  2   | vhp000cc | Attributes                           |
    //|      |          |   v = Vertical Flip   (1=Flip)       |
    //|      |          |   h = Horizontal Flip (1=Flip)       |
    //|      |          |   p = Background Priority            |
    //|      |          |         0 = In front                 |
    //|      |          |         1 = Behind                   |
    //|      |          |   c = Upper two (2) bits of colour   |
    //|  3   | XXXXXXXX | X Coordinate (upper-left corner)     |
    //+------+----------+--------------------------------------+}
    
    *((unsigned char*)0x200) = 0x50;
    *((unsigned char*)0x201) = 0x00;
    *((unsigned char*)0x202) = 0x00;
    *((unsigned char*)0x203) = 0x50;
    
    gX = 0x50;
    gY = 0x50;
    gXScroll = 0;
    gYScroll = 0;
    gXNametable = 0;
    
    
}

void dma_sprites(void)
{
    *((unsigned char*)0x4013) = 0x00;
    *((unsigned char*)0x4014) = 0x02;
}

void input_poll(void)
{
    // Strobe 0x4016 bit 0 to gather input
    *((unsigned char*)0x4016) |= 0x01;
    *((unsigned char*)0x4016) &= 0xFE;

    gController1 = 0x00;
    
     if(*((unsigned char*)0x4016) & 0x01)
     {
         gController1 |= C1_A;
     }
     if(*((unsigned char*)0x4016) & 0x01)
     {
         gController1 |= C1_B;
     }
     if(*((unsigned char*)0x4016) & 0x01)
     {
         gController1 |= C1_s;
     }
     if(*((unsigned char*)0x4016) & 0x01)
     {
         gController1 |= C1_S;
     }
     if(*((unsigned char*)0x4016) & 0x01)
     {
         gController1 |= C1_U;
     }
     if(*((unsigned char*)0x4016) & 0x01)
     {
         gController1 |= C1_D;
     }
     if(*((unsigned char*)0x4016) & 0x01)
     {
         gController1 |= C1_L;
     }
     if(*((unsigned char*)0x4016) & 0x01)
     {
         gController1 |= C1_R;
     }
         
}

void update_sprites(void)
{
    if(gController1 & C1_U)
    {
        if(gY > 0x30)
        {
            gY -= 1;
        }
        else
        {
            gYScroll -= 1;
        }
    }
    if(gController1 & C1_D)
    {
        if(gY < 0xC0)
        {
            gY += 1;
        }
        else
        {
            gYScroll += 1;
        }
    }
    if(gController1 & C1_L)
    {
        if(gX > 0x30)
        {
            gX -= 1;
        }
        else
        {
            gXScroll -= 1;
            
            // swap nametables and reset the X scroll if we've fully wrapped
            if(gXScroll == 0xFF)
            {
                gXNametable = 1-gXNametable;
                gXScroll = 0xFF;
            }
        }
    }
    if(gController1 & C1_R)
    {
        if(gX < 0xC0)
        {
            gX += 1;
        }
        else
        {
            gXScroll += 1;
            
            // swap nametables and reset the X scroll if we've fully wrapped
            if(gXScroll == 0x00)
            {
                gXNametable = 1-gXNametable;
                gXScroll = 0;
            }
        }
    }
    if(gController1 & C1_B)
    {
        ////raw period = 111860.8/frequency - 1
        //
        //// Make a different sound
        //
        //// raw period
        //*((unsigned char*)0x4003) = 0x01;
        //*((unsigned char*)0x4002) = 0x73;
        //// duty cycle and volume
        //*((unsigned char*)0x4000) = 0xBF;
        
        // Play the music
        pMusicPlay();
        
    }
    if(gController1 & C1_A)
    {
        //raw period = 111860.8/frequency - 1
        
        // Make a sound
        
        // raw period
        *((unsigned char*)0x4003) = 0x01;
        *((unsigned char*)0x4002) = 0x17;
        // duty cycle and volume
        *((unsigned char*)0x4000) = 0xBF;
    }
    if(!((gController1 & C1_A) || (gController1 & C1_B)))
    {
        // Stop making the sound
        //*((unsigned char*)0x4000) = 0x30;
    }
        
    
    *((unsigned char*)0x200) = gY;
    *((unsigned char*)0x203) = gX;
}

void scroll_right(void)
{
    // set bits [1:0] to 0 for nametable
    *((unsigned char*)0x2000) = 0x88 + gXNametable;
    // read 0x2002 to reset the address toggle
    devnull = *((unsigned char*)0x2002);
    // scroll a number of pixels in the X direction
    *((unsigned char*)0x2005) = gXScroll;
    // scroll a number of pixels in the Y direction
    *((unsigned char*)0x2005) = gYScroll;
}

void main(void)
{
    vblank();
    vblank();
    vblank();
    vblank();
    vblank();
    
    ppudisable();
    
    palattes();
    nametables();
    sprites();
    
    apuinit();
    
    pMusicInit(0x1);
    
    vblank();
    vblank();
    vblank();
    vblank();
    vblank();

    ppuinit();
    
    while(1)
    {
        vblank();
        
        input_poll();
        
        update_sprites();
        
        dma_sprites();
        
        scroll_right();
        
    }
}

